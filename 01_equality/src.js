module.exports = {

  sameLength: function(string1, string2) {
    return string1.length === string2.length
  },

  firstGreater: function(arg, argh) {
    return arg > argh
  },

  firstGreaterOrEqualTo: function(arg, argh) {
    return arg >= argh
  },

  firstLengthGreater: function(arg, argh) {
    return arg.length > argh.length
  },

  secondGreater: function(arg, argh) {
    return arg < argh
  }




}
